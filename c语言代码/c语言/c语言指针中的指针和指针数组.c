#include<stdio.h>
int main()
{
    // 定义一个字符指针数组，每个元素指向一个字符串
    char *c[]={
        "hello ","world", "!",  "how","are","you"
    };
    // 定义一个指向字符指针的指针，指向数组c的第一个元素
    char **p=c;
    // 定义一个字符指针数组，用于存储指向c数组中某些元素的指针
    char **jia[4];
    // 将指针p指向c数组的最后一个元素（即"you"）
    p=&c[4];
    // 将jia数组的第一个元素指向c数组的第一个元素（即"hello "）
    jia[0]=&c[0];
    // 将jia数组的第二个元素指向c数组的第二个元素（即"world"）
    jia[1]=&c[1];
    // 将jia数组的第三个元素指向c数组的第三个元素（即"!"）
    jia[2]=&c[2];
    // 将jia数组的第四个元素指向c数组的第四个元素（即"how"）
    jia[3]=&c[3];
    // 打印p指向的字符串，即"you"
    printf("%s\n",*p);
    // 遍历jia数组，打印每个元素指向的字符串
    for (int i=0;i<4;i++)
    {
        // 打印jia[i]指向的字符串
        printf("%s\n",*jia[i]);
    }
    return 0;
}