#include<stdio.h>
int main()
{
    // 定义六个整型变量a, b, c, d, e, f，并分别赋值为1, 2, 3, 4, 5, 6
    int a=1;
    int b=2;
    int c=3;
    int d=4;
    int e=5;
    int f=6;
  
    // 定义一个整型指针数组x，包含6个元素，每个元素是指向整型变量的指针
    // 将x数组的每个元素分别指向a, b, c, d, e, f
    int *x[6]={ &a, &b,&c,&d,&e,&f};
 
    // 定义一个指向整型指针的指针p，并将其指向x数组的第一个元素
    int **p;
   
    // 定义一个指向整型指针的指针数组jia，包含4个元素
    int **jia[4];
  
    // 将p指向x数组的最后一个元素（即指向f的指针）
    p=&x[5];
   
    // 将jia数组的第一个元素指向x数组的第一个元素（即指向a的指针）
    jia[0]=&x[0];
   
    // 将jia数组的第二个元素指向x数组的第二个元素（即指向b的指针）
    jia[1]=&x[1];
   
    // 将jia数组的第三个元素指向x数组的第三个元素（即指向c的指针）
    jia[2]=&x[2];
   
    // 将jia数组的第四个元素指向x数组的第四个元素（即指向d的指针）
    jia[3]=&x[3];
    
    // 打印p指向的指针所指向的值，即f的值
    printf("%d\n",**p);
 
    // 遍历jia数组，打印每个元素指向的指针所指向的值
    for (int i=0;i<4;i++)
    {
     
        // 打印jia[i]指向的指针所指向的值，即x[i]指向的值
        printf("%d\n",**jia[i]);
    }
    return 0;
}